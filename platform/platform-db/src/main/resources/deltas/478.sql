CREATE TABLE CURRENCY (
  CURRENCY_CODE VARCHAR(3) NOT NULL PRIMARY KEY,
  CURRENCY_LABEL VARCHAR(3) NOT NULL,
  COMMISSION_MULTIPLIER DECIMAL(6,3) NOT NULL DEFAULT 1.0
) charset=utf8#

